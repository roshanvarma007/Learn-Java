public class Unary {

    public static void main(String[] args) {
int a = 10;
a++;
a++;
//unary operator addition
System.out.println(a);

int b = 20;
b--;          // pre unary
--b;          //post unary

//unary operator substraction
System.out.println(b);

int c = 25;
c--;
c++;
System.out.println(--c);
System.out.println(++c);


int d = 30;
System.out.println(~d);      //tilt(~) use for minus in front

boolean bool1 = true;

System.out.println(!bool1);   //exlametry (!) mark will show oppostite value


    }
    
}
